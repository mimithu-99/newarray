public class ForEachLoop {
    public static void main(String[] args) {
        String [] names  = new String[4];

        names [0] = "Mim";
        names [1] = "Bindu";
        names [2] = "Muntaha";
        names [3] = "Toya";

        for (int i = 0; i < 4 ; i++) {
            System.out.println(names[i]);
            
        }

    }
}
